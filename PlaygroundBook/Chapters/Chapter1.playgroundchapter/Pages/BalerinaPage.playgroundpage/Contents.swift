 # Rotation
 
 Look at the pictures, can you see the difference between ballerina's positions? In which case, the rotational speed is higher?
 
 On the first one ballerina has a higher moment of inertia, because she has her arms and leg extended. And on the second picture ballerina refracted her leg and arms.
 
 In physics, there is [conservation of angular momentum](glossary://Conservation%20Of%20Angular%20Momentum), and because of that if the [moment of inertia](glossary://Moment%20Of%20Inertia) increases the [rotational speed](glossary://Rotational%20Speed) decreases and opposite if the [moment of inertia](glossary://Moment%20Of%20Inertia) decreases the [rotational speed](glossary://Rotational%20Speed) increases.
 
 So if ballerina changed her position from 1 to 2, her [rotational speed](glossary://Rotational%20Speed) increased.
 
